sales = [1500, 3000, 500, 4200, 2000, 0, 7500, 3200]

# เรียงลำดับจากมากไปน้อย
sales.sort(reverse=True)
print(f"ยอดขายเรียงจากมากไปน้อย: {sales}")

# เอายอดขาย 3 อันดับแรก
ranked_sales = sales[:3]
print(f"ยอดขาย 3 อันดับแรก: {ranked_sales}")
print(f"---" * 10)

# หายอดขายที่เป็น 0 (ถ้ามี)
if 0 in sales:
    print("ยังมีหนังสือที่ขายไม่ออก")
else:
    print("ไม่มีหนังสือที่ขายไม่ออก")