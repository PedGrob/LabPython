# โจทย์: คุณมีข้อมูลรายชื่อลูกค้าใหม่จาก สาขา A และ สาขา B ต้องการรวมรายชื่อทั้งหมดเข้าด้วยกันเพื่อส่งเข้าฐานข้อมูลกลาง

# ให้นำข้อมูลทั้งสองก้อนมาต่อกัน
# เรียงลำดับชื่อลูกค้าใหม่จาก A -> Z
# นับจำนวนลูกค้าทั้งหมด (len) และแสดงผล

branch_a = ["Bob", "Alice"]
branch_b = ["Charlie", "Eve", "David"]

# ให้นำข้อมูลทั้งสองก้อนมาต่อกัน
all_customers = branch_a + branch_b

# เรียงลำดับชื่อลูกค้าใหม่จาก A -> Z
all_customers.sort()

# นับจำนวนลูกค้าทั้งหมด (len) และแสดงผล
print("รายชื่อลูกค้าทั้งหมด:", all_customers)
print("จำนวนลูกค้าทั้งหมด:", len(all_customers))

