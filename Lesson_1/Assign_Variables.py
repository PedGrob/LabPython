# สร้างตัวแปร (Assign variables)
shop_name = "Cafe Amazonian"  # ชื่อร้าน
coffee_price = 155         # ราคากาแฟต่อแก้ว
daily_customers = 120     # จำนวนลูกค้าวันนี้
Americano_sold = 75       # จำนวนกาแฟอเมริกาโน่ที่ขายได้
total_revenue = coffee_price * daily_customers  # รายได้รวมต่อวัน

# ลองเรียกดูของในกล่อง
print("ชื่อร้าน", shop_name)
print(coffee_price)
print(daily_customers)
print(Americano_sold)
print(total_revenue, "บาท")