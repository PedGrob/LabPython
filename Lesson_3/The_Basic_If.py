# ตัวอย่างระบบตรวจสอบยอดคงเหลือ

balance = 500         # เงินในบัญชี
withdraw = 200       # จะถอนเงิน

print("กำลังทำรายการ...")

# เริ่มสร้างเงื่อนไข
if withdraw > balance:
  # สังเกตการย่อหน้าตรงนี้!
  print("Error: ยอดเงินไม่พอถอน")
  print("กรุณาเติมเงินก่อนทำรายการ")

  if withdraw > 10000:
    print("แจ้งเตือน: การถอนเกิน 10,000 บาท ต้องยืนยันตัวตน")  

print("จบการทำงาน")